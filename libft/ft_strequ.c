#include <stdio.h>

int	ft_strequ(const char *s1, const char *s2)
{
	if (!s1 || !s2)
		return (1);
	while (*s1)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	if ((!*s1 && *s2) || (!*s2 && *s1))
		return (0);
	return (1);
}
